# 2. Tab: Opportunity Wizard & Approval Gate
with tab_wizard:
    st.header("Opportunity Tracking & Asset Generator")
    
    jobs = db.query(JobOpportunityTable).all()
    if not jobs:
        st.warning("No opportunities in the database yet. Click 'Run Auto-Discovery' in the sidebar to populate.")
    else:
        job_options = {f"{j.company_name} - {j.title} (ID: {j.id}, Status: {j.status})": j.id for j in jobs}
        selected_job_label = st.selectbox("Select Target Opportunity", list(job_options.keys()))
        selected_job_id = job_options[selected_job_label]
        
        job = db.query(JobOpportunityTable).filter(JobOpportunityTable.id == selected_job_id).first()
        
        # Display Job details
        st.markdown(f"### **{job.title}** at **{job.company_name}**")
        st.caption(f"Source: {job.source} | URL: {job.url if job.url else 'N/A'}")
        
        # In-line Status Modifier
        col_st1, col_st2 = st.columns([1, 2])
        with col_st1:
            status_opts = ["discovered", "applied", "OA", "Interview", "Offer", "Rejected"]
            current_status_idx = status_opts.index(job.status) if job.status in status_opts else 0
            new_status = st.selectbox("Change Status", status_opts, index=current_status_idx)
            if new_status != job.status:
                job.status = new_status
                # If status is applied but no application object exists, create a basic one
                if new_status == "applied":
                    app_exists = db.query(ApplicationTable).filter(ApplicationTable.job_id == job.id).first()
                    if not app_exists:
                        app = ApplicationTable(candidate_id=1, job_id=job.id, status="Applied")
                        db.add(app)
                elif new_status == "Interview":
                    # Update status of application
                    app = db.query(ApplicationTable).filter(ApplicationTable.job_id == job.id).first()
                    if app:
                        app.status = "Interview"
                db.commit()
                st.success("Opportunity status updated!")
                st.rerun()

        # Job Description Viewer
        with st.expander("Show Job Description Text"):
            st.text(job.jd_text)
            
        st.markdown("---")
        st.subheader("Asset Generation Pipeline")
        
        # Choose Template
        template_name = st.selectbox("Select LaTeX Template Style", ["backend.tex", "frontend.tex", "analytics.tex", "ai.tex", "general.tex"])
        
        col_act1, col_act2 = st.columns(2)
        with col_act1:
            if st.button("🚀 Run Tailoring Orchestrator", use_container_width=True):
                with st.spinner("Processing company news, matching credentials, and optimizing ATS scores..."):
                    res = run_application_pipeline(db, job.id, template_name)
                    if res["success"]:
                        st.success("Assets tailored and validated successfully!")
                        st.balloons()
                        st.rerun()
                    else:
                        st.error(f"Pipeline Execution Failed: {res.get('error')}")
                        if "report" in res:
                            st.subheader("Critic Validation Errors:")
                            for err in res["report"]["errors"]:
                                st.error(err)
                            st.subheader("Critic Warnings:")
                            for warn in res["report"]["warnings"]:
                                st.warning(warn)
                                
        # Render generated assets if they exist
        app = db.query(ApplicationTable).filter(ApplicationTable.job_id == job.id).first()
        if app and app.cover_letter_path:
            st.markdown("### **Review Generated Materials**")
            
            # Sub-tabs for reviewing generated output
            tab_res, tab_cl, tab_net, tab_exp = st.tabs(["📄 Resume Version", "✉️ Cover Letter", "💬 LinkedIn Outreach", "🧠 Explainability Report"])
            
            with tab_res:
                st.info(f"Resume generated under data/generated. Variant ID: {app.resume_variant_id}")
                variant = db.query(ResumeVariantTable).filter(ResumeVariantTable.id == app.resume_variant_id).first()
                if variant:
                    st.text_area("LaTeX Source Code", value=variant.latex_source, height=350)
                    
            with tab_cl:
                if os.path.exists(app.cover_letter_path):
                    with open(app.cover_letter_path, "r", encoding="utf-8") as f:
                        st.markdown(f.read())
                        
            with tab_net:
                if os.path.exists(app.outreach_sequence_path):
                    with open(app.outreach_sequence_path, "r", encoding="utf-8") as f:
                        st.text(f.read())
                        
            with tab_exp:
                safe_company_name = job.company_name.replace(" ", "_")
                exp_path = Path(app.cover_letter_path).parent / f"{safe_company_name}_explainability.json"
                if exp_path.exists():
                    with open(exp_path, "r", encoding="utf-8") as f:
                        exp_data = json.load(f)
                    st.json(exp_data)
                    
                    st.markdown("#### **Selection Decisional Path**")
                    st.write(f"**Job Fit Score**: `{exp_data['decision_parameters']['job_fit_score']}%` (Threshold: `{exp_data['decision_parameters']['apply_threshold']}%`)")
                    st.write(f"**ATS Optimization Progression**: `{exp_data['ats_score_comparison']['initial_percent']}%` ➔ `{exp_data['ats_score_comparison']['final_percent']}%` (in `{exp_data['ats_score_comparison']['iterations_run']}` iterations)")
                    
                    col_sel1, col_sel2 = st.columns(2)
                    with col_sel1:
                        st.markdown("**Selected Project Nodes**")
                        for p in exp_data["project_selection"]["selected"]:
                            st.success(f"✓ **{p['name']}**: {p['reason']}")
                    with col_sel2:
                        st.markdown("**Rejected Project Nodes**")
                        for p in exp_data["project_selection"]["rejected"]:
                            st.warning(f"✗ **{p['name']}**: {p['reason']}")
                else:
                    st.warning("No explainability log found for this application. Re-run the tailoring pipeline.")


