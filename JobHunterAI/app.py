# 5. Tab: Offer Evaluator
with tab_offers:
    st.header("Deterministic Offer Evaluation Engine")
    st.markdown("Compare multiple offers side-by-side based on financial metrics, equity growth, remote policies, and branding.")
    
    # State-based list of offers to compare
    if 'offers' not in st.session_state:
        st.session_state['offers'] = [
            {"company_name": "Stripe (Sample)", "base_salary": 180000, "equity_annual_val": 40000, "work_policy": "Remote", "company_stage": "Late Stage"},
            {"company_name": "AI Startup (Sample)", "base_salary": 140000, "equity_annual_val": 60000, "work_policy": "Hybrid", "company_stage": "YC Seed"}
        ]
        
    # Input Form
    with st.form("add_offer_form"):
        st.subheader("Add an Offer to Compare")
        o_company = st.text_input("Company Name")
        o_salary = st.number_input("Base Salary ($)", min_value=0, value=150000)
        o_equity = st.number_input("Annual Equity Value ($)", min_value=0, value=30000)
        o_policy = st.selectbox("Work Policy", ["Remote", "Hybrid", "Onsite"])
        o_stage = st.text_input("Company Stage / Info (e.g. YC Seed, Series A, Late Stage)")
        
        submitted = st.form_submit_button("Add Offer")
        if submitted and o_company:
            st.session_state['offers'].append({
                "company_name": o_company,
                "base_salary": o_salary,
                "equity_annual_val": o_equity,
                "work_policy": o_policy,
                "company_stage": o_stage
            })
            st.success(f"Added offer from {o_company}!")
            st.rerun()
            
    # Display comparison
    if st.session_state['offers']:
        st.markdown("### Comparison Standings")
        ranked = compare_offers(st.session_state['offers'])
        
        df_ranked = pd.DataFrame(ranked)
        st.dataframe(df_ranked, use_container_width=True)
        
        # Clear button
        if st.button("Clear Offers"):
            st.session_state['offers'] = []
            st.rerun()
