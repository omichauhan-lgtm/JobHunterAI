# 0. Tab: Daily Briefing & Queue
with tab_queue:
    st.header("Daily Career Briefing & Action Queue")
    
    # Trigger Autonomous Daily Loop
    col_trigger1, col_trigger2 = st.columns([2, 1])
    with col_trigger1:
        st.write("Run the autonomous pipeline loop manually to scrape jobs, classify remote tiers, rank tech stack match, and deliver the daily HTML brief.")
    with col_trigger2:
        if st.button("⚡ Trigger Daily Autonomous Loop", use_container_width=True):
            with st.spinner("Executing discovery scraper, dynamic matching ranking, and sending daily briefing report..."):
                from orchestrator import run_daily_autonomous_loop
                res = run_daily_autonomous_loop(db)
                st.success(f"Daily loop run complete! Processed: {res['processed']}, Matched: {res['matched']}, Briefing Delivered: {res['briefing_sent']}")
                st.rerun()

    st.markdown("---")
    
    # Render Latest Daily Briefing HTML
    st.subheader("📰 Latest Daily Career Briefing")
    import glob
    briefing_files = glob.glob(str(GEN_DIR / "daily_briefing_*.html"))
    if briefing_files:
        # Sort to get the latest file
        briefing_files.sort(reverse=True)
        latest_brief = briefing_files[0]
        try:
            with open(latest_brief, "r", encoding="utf-8") as f:
                html_brief = f.read()
            st.components.v1.html(html_brief, height=450, scrolling=True)
        except Exception as e:
            st.error(f"Error loading daily brief: {e}")
    else:
        st.info("No daily briefing reports generated yet. Trigger the daily loop above to build your first report!")
        
    st.markdown("---")
    
    # Application Queue
    st.subheader("📥 Actionable Match Queue")
    st.write("Review auto-tailored application variants generated by the daily loop before final manual submission.")
    
    # Query matching opportunities
    matching_jobs = db.query(JobOpportunityTable).filter(
        JobOpportunityTable.overall_score >= APPLY_THRESHOLD,
        JobOpportunityTable.status == "discovered"
    ).all()
    
    if not matching_jobs:
        st.success("All high-matching jobs have been reviewed! Run auto-discovery or trigger the daily loop to ingest more opportunities.")
    else:
        # Format table data
        queue_data = []
        for j in matching_jobs:
            app = db.query(ApplicationTable).filter(ApplicationTable.job_id == j.id).first()
            status_text = "✅ Ready" if app and app.cover_letter_path else "⏳ Tailoring Pending"
            queue_data.append({
                "ID": j.id,
                "Company": j.company_name,
                "Job Title": j.title,
                "Match Score": f"{j.overall_score}%",
                "Status": status_text
            })
            
        st.dataframe(pd.DataFrame(queue_data), use_container_width=True)
        
        # Action selector
        st.markdown("#### **Quick Review Action Center**")
        selected_queue_id = st.selectbox(
            "Select Opportunity to Review/Approve",
            options=[j.id for j in matching_jobs],
            format_func=lambda x: next(f"{j.company_name} - {j.title} (Score: {j.overall_score}%)" for j in matching_jobs if j.id == x)
        )
        
        if selected_queue_id:
            job_to_review = db.query(JobOpportunityTable).filter(JobOpportunityTable.id == selected_queue_id).first()
            app_to_review = db.query(ApplicationTable).filter(ApplicationTable.job_id == selected_queue_id).first()
            
            if app_to_review:
                col_btn1, col_btn2 = st.columns(2)
                with col_btn1:
                    if st.button("👍 Approve & Mark as Applied", key="approve_app"):
                        job_to_review.status = "applied"
                        app_to_review.status = "Applied"
                        db.commit()
                        st.success(f"Successfully marked application to {job_to_review.company_name} as Applied!")
                        st.balloons()
                        st.rerun()
                with col_btn2:
                    st.caption("Use 'Opportunity Wizard' tab to edit source code and customize materials prior to approval.")
            else:
                if st.button("⚙️ Trigger Manual Tailoring", key="tailor_manual"):
                    with st.spinner("Compiling tailored resume..."):
                        run_application_pipeline(db, selected_queue_id)
                        st.success("Assets compiled successfully!")
                        st.rerun()

